#include "libft.h"

int		ft_atoi(const char *str)
{
	size_t	i;
	size_t	nb;
	int		sign;

	i = 0;
	nb = 0;
	sign = 0;
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '-')
		sign = 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + str[i] - '0';
		i++;
	}
	return (sign ? -nb : nb);
}
